<section class="tight" aria-labelledby="che-cecite-introduction-heading">
    <h2 id="che-cecite-introduction-heading" class="visually-hidden">Introduction</h2>
    <p>
        Le CJENS C&eacute;cit&eacute; accueille des jeunes malvoyants ou aveugles pr&eacute;sentant un polyhandicap,
        de l&rsquo;enfance jusqu&rsquo;&agrave; l&rsquo;entr&eacute;e dans la vie adulte.
        Notre &eacute;quipe pluridisciplinaire (&eacute;ducateurs, kin&eacute;, logo, ergo, psy, AS)
        propose des journ&eacute;es structur&eacute;es, s&eacute;curisantes et stimulantes pour d&eacute;velopper les gestes du quotidien,
        la communication et la participation.
        Ici, on avance au rythme de chacun, avec bienveillance et respect, pour gagner en autonomie&hellip;
        m&ecirc;me petite, mais durable.
    </p>
</section>


