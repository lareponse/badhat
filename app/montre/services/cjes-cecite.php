<section class="tight" aria-labelledby="cjes-dv-public-heading">
    <h2 id="cjes-dv-public-heading">Public accueilli</h2>
    <ul>
        <li>Déficience visuelle sévère (p. ex. acuité ≤ 3/10) ou cécité.</li>
        <li>Profils parfois associés (troubles médicaux, intellectuels, santé mentale) pris en compte au cas par cas.</li>
        <li>Porte d’entrée par la déficience visuelle ; accompagnement individualisé.</li>
    </ul>
</section>

<section class="tight" aria-labelledby="cjes-dv-equipe-heading">
    <h2 id="cjes-dv-equipe-heading">Équipe pluridisciplinaire</h2>
    <ul>
        <li>Éducateurs (socle du quotidien)</li>
        <li>Logopède, ergothérapeute, kinésithérapeute, psychomotricien·ne</li>
        <li>Psychologue / assistant·e social·e (suivi, lien familles)</li>
        <li>Spécialiste basse vision (interventions régulières)</li>
        <li>Coordination par les chefs de projet (CP), en lien étroit avec l’école</li>
    </ul>
</section>

<section class="tight" aria-labelledby="cjes-dv-journee-heading">
    <h2 id="cjes-dv-journee-heading">Une journée type</h2>
    <ul>
        <li><strong>08:00–08:30</strong> : accueil CJES et accompagnement vers l’école</li>
        <li><strong>08:30–12:00</strong> : cours + éventuelles prises en charge individuelles (logo/ergo/…)</li>
        <li><strong>12:00/12:30–13:30</strong> : repas et activités au CJES (vie quotidienne, autonomie)</li>
        <li><strong>13:30–15:00</strong> : retour en classe / prises en charge</li>
        <li><strong>15:00–16:15</strong> : CJES (goûter, jeux, ateliers, retours) puis transport COCOF</li>
    </ul>

    <p>
        Les horaires varient selon l’âge et la section (au secondaire, fenêtres typiques : <strong>12:30–13:30</strong>
        et <strong>15:10–16:00</strong>).
    </p>
</section>

<section class="tight" aria-labelledby="cjes-dv-activites-heading">
    <h2 id="cjes-dv-activites-heading">Activités régulières &amp; ressources</h2>
    <ul>
        <li>Piscine (découverte, aisance, schéma corporel, plaisir)</li>
        <li>Hypothérapie (médiation cheval, relation, posture, confiance)</li>
        <li>EVRAS (cellule dédiée : « bulle », relations &amp; affectif)</li>
        <li>Outils adaptés : Braille, contrastes, vaisselle et couverts adaptés, dispositifs parlants, etc.</li>
        <li>Inclusion : activités sur site et à l’extérieur, en lien avec l’école et les partenaires</li>
    </ul>
</section>

<section class="tight" aria-labelledby="cjes-dv-groupes-heading">
    <h2 id="cjes-dv-groupes-heading">Groupes</h2>
    <ul>
        <li><strong>Maternelles :</strong> Lucioles</li>
        <li><strong>Primaires :</strong> Caméléons</li>
        <li><strong>Secondaire :</strong> Ados et Artistes (forme 2)</li>
    </ul>
</section>

<section class="tight" aria-labelledby="cjes-dv-admission-heading">
    <h2 id="cjes-dv-admission-heading">Modalités d’admission</h2>
    <ol>
        <li>Premier contact (mail / téléphone).</li>
        <li>Analyse de la demande par la direction d’école et le CP (bilans récents souhaités).</li>
        <li>Rencontre famille + visite : explications, adaptations possibles.</li>
        <li>Inscription (dossier école + CJES).</li>
    </ol>

    <p>
        Les enfants déjà scolarisés à l’IRSA peuvent intégrer le CJES via une procédure simplifiée.
    </p>
</section>

<section class="tight" aria-labelledby="cjes-dv-distinctif-heading">
    <h2 id="cjes-dv-distinctif-heading">Ce qui nous distingue</h2>
    <ul>
