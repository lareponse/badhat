<section class="tight" aria-labelledby="cjes-dv-presentation-heading">
    <h2 id="cjes-dv-presentation-heading" class="visually-hidden">Présentation du CJES</h2>

    <div class="triple-infos">
        <article>
            <h3>Public</h3>
            <p>Enfants et adolescents avec déficience visuelle importante</p>
        </article>

        <article>
            <h3>Tranche d'âge</h3>
            <p>3 à 21 ans</p>
        </article>

        <article>
            <h3>Accompagnements</h3>
            <p>Scolaire + éducatif + thérapeutique, en synergie avec l’école</p>
        </article>
    </div>

    <p>
        Le CJES « Déficience visuelle » accompagne des élèves malvoyants ou aveugles pendant les temps hors classe
        et en co-intervention à l’école. Les équipes éducatives et paramédicales travaillent avec les instituteurs
        pour proposer des aménagements pédagogiques, éducatifs et thérapeutiques adaptés, dans un cadre bienveillant,
        sécurisant et respectueux.
    </p>
</section>

