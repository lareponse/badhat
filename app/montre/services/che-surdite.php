        <dd>Présence éducative continue</dd>
    </dl>
</section>

<section class="tight" aria-labelledby="activites-heading">
    <h2 id="activites-heading">Activités et projets</h2>
    <ul>
        <li>Communication et expression : activités en langue des signes, ateliers d’expression orale ou visuelle</li>
        <li>Vie quotidienne accompagnée : préparation du sac, rangement des effets personnels, organisation du matériel scolaire ou auditif</li>
        <li>Bien-être et détente : snoezelen, temps calmes individualisés, massages</li>
        <li>Loisirs adaptés : jeux collaboratifs, plaine de jeux, sorties extérieures, activités sportives adaptées</li>
        <li>Ateliers créatifs : arts plastiques, musique visuelle, projets réalisés avec la ludothèque</li>
        <li>Développement des compétences sociales : jeux de rôle, activités de groupe, projets collectifs</li>
    </ul>

    <p>
        Chaque activité vise à renforcer l’autonomie, la communication et la participation active.
    </p>
</section>

<section class="tight" aria-labelledby="admissions-heading">
    <h2 id="admissions-heading">Admissions</h2>
    <ol>
        <li>Premier contact avec l’IRSA, parfois par l’école ou le centre de jour</li>
        <li>Visite de l’institution (si souhaitée)</li>
        <li>Dossier d’admission préparé par l’assistant social (informations médicales, audiologiques, scolaires et administratives)</li>
        <li>Commission d’admission : direction, médecin, assistant social, école ou PMS</li>
        <li>Décision d’admission ou période d’essai</li>
        <li>Rencontre préalable avec la famille pour organiser l’intégration (matériel auditif, rituels du coucher, besoins spécifiques, communication utilisée)</li>
        <li>Transmission interne à l’équipe éducative pour un accueil fluide et cohérent</li>
    </ol>

    <p>
        Les visites en semaine sont réservées aux jeunes placés. Les familles gardent un contact régulier par téléphone
        ou lors du retour week-end.
    </p>
</section>

