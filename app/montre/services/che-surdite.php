<section class="tight" aria-labelledby="che-cecite-introduction-heading">
    <h2 id="che-cecite-introduction-heading" class="visually-hidden">Introduction</h2>
    <p>
        Le CHE Surdité accueille des enfants et des jeunes présentant une déficience auditive ou une surdité,
        parfois associée à d’autres particularités de développement.
    </p>

    <p>
        L’hébergement offre un cadre sécurisant et chaleureux, pensé pour assurer le bien-être des enfants
        lorsque le trajet scolaire est trop long, lorsque la famille a besoin d’un relais dans le quotidien,
        ou dans le cadre de certains placements.
    </p>

    <p>
        L’équipe veille à soutenir chaque jeune dans son autonomie, à encourager la communication sous toutes ses formes
        et à maintenir un lien constant avec les familles.
    </p>

    <article aria-labelledby="che-public-heading">
        <h3 id="che-public-heading">Public accueilli</h3>
        <ul>
            <li>Enfants et adolescents sourds ou malentendants, avec ou sans troubles associés (intellectuels, médicaux ou comportementaux)</li>
            <li>Âges accueillis : de 3 à 21 ans, selon le parcours et les dérogations scolaires</li>
        </ul>
    </article>
</section>

<section class="tight" aria-labelledby="missions-heading">
    <h2 id="missions-heading">Nos missions</h2>
    <ul>
        <li>Garantir un cadre de vie sécurisant, bienveillant et adapté aux besoins des jeunes sourds et malentendants</li>
        <li>Développer l’autonomie dans les gestes du quotidien et les habitudes de vie</li>
        <li>Encourager l’expression et la communication : orale, signée ou alternative</li>
        <li>Maintenir une collaboration régulière avec les familles pour soutenir l’évolution de chaque enfant</li>
        <li>Offrir un environnement stimulant et valorisant</li>
    </ul>
</section>

<section class="tight" aria-labelledby="equipe-heading">
    <h2 id="equipe-heading">Équipe et métiers</h2>
    <ul>
        <li>Éducateurs et éducatrices de référence : suivi quotidien, activités et rituels</li>
        <li>Assistant social : coordination des admissions et lien avec les familles</li>
        <li>Psychologue : soutien individualisé et accompagnement des équipes</li>
        <li>Infirmière : suivi de santé et coordination médicale</li>
        <li>Chef de projet : coordination générale du centre</li>
        <li>Collaboration avec le centre de jour et les paramédicaux : logopédie, psychomotricité, ergothérapie, communication, outils visuels et langues des signes</li>
    </ul>
</section>

<section class="tight" aria-labelledby="journee-heading">
    <h2 id="journee-heading">Une journée type</h2>
    <dl class="grid">
        <dt>Matin</dt>
        <dd>Lever, hygiène, petit-déjeuner, préparation et départ vers l’école en fonction du niveau scolaire</dd>

        <dt>16h00 – 18h00</dt>
        <dd>Arrivée des jeunes, accueil individualisé, goûter, temps libre ou activités</dd>

        <dt>18h00 – 19h30</dt>
        <dd>Repas, soins, temps de détente, moments d’échange en petits groupes</dd>

        <dt>19h30 – 21h00</dt>
