<section class="tight" aria-labelledby="missions-heading">
    <h2 id="missions-heading">Nos missions</h2>
    <ul>
        <li>Favoriser le bien-être et la sécurité affective</li>
        <li>Développer les compétences de vie quotidienne (hygiène, repas, rangement, habillage, etc.)</li>
        <li>Encourager l’autonomie et l’autodétermination dans le respect du rythme de chacun</li>
        <li>Maintenir des liens solides avec les familles, dans une logique de confiance et de collaboration</li>
        <li>Offrir un cadre de vie chaleureux, propice à la détente et au développement personnel</li>
    </ul>
</section>

<section class="tight" aria-labelledby="equipe-heading">
    <h2 id="equipe-heading">Équipe &amp; métiers</h2>
    <ul>
        <li>Éducateurs·trices de référence : suivi quotidien, activités et rituels</li>
        <li>Assistant·e social·e : fil conducteur des admissions et du suivi familial</li>
        <li>Psychologue : accompagnement des jeunes et des équipes</li>
        <li>Infirmière : suivi de santé et coordination médicale</li>
        <li>Chef de projet (CP) : coordination générale du centre</li>
        <li>Collaboration avec les paramédicaux et le centre de jour : logopédie, ergothérapie, psychomotricité, basse vision…</li>
    </ul>
</section>

