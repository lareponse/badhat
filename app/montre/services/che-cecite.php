<section class="tight" aria-labelledby="activites-heading">
    <h2 id="activites-heading">Activités &amp; projets</h2>
    <ul>
        <li>Vie quotidienne guidée : apprentissage des gestes autonomes (préparer son sac, ranger, se servir, repérer ses affaires)</li>
        <li>Bien-être et détente : snoezelen, bains sensoriels, massages, musique, moments calmes individualisés</li>
        <li>Loisirs adaptés : vélo tandem, plaine de jeux, sorties extérieures, art et musicothérapie</li>
        <li>Projet artistique et d’expression (mercredis avec la ludothèque)</li>
        <li>Art-thérapie &amp; jeux de rôle (avec la psychologue)</li>
        <li>Activités sportives adaptées (avec les éducateurs spécialisés)</li>
    </ul>

    <p>
        Chaque moment est pensé pour être plaisant, éducatif et valorisant.
    </p>
</section>

<section class="tight" aria-labelledby="admissions-heading">
    <h2 id="admissions-heading">Admissions</h2>
    <ol>
        <li>Premier contact (école, centre ou famille) et visite si souhaitée</li>
        <li>Constitution d’un dossier d’admission par l’assistant·e social·e (informations médicales, administratives, éducatives)</li>
        <li>Commission d’admission : direction, médecin, assistant social, école (ou PMS selon cas)</li>
        <li>Décision d’admission ou période d’essai si nécessaire</li>
        <li>Rencontre préalable avec la famille pour définir les habitudes de vie (repas, sommeil, soins, besoins spécifiques)</li>
        <li>Transmission interne aux éducateurs pour assurer une intégration fluide</li>
    </ol>

    <p>
        Les visites en semaine sont réservées aux enfants placés (visites encadrées).
        Les familles gardent contact principalement par téléphone et lors du retour week-end.
    </p>
</section>

