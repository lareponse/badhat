<section class="tight" aria-labelledby="che-cecite-introduction-heading">
    <h2 id="che-cecite-introduction-heading" class="visually-hidden">Introduction</h2>

    <p>
        Le CHE – Cécité accueille des enfants et jeunes présentant une déficience visuelle,
        parfois associée à d’autres troubles.
    </p>

    <p>
        L’hébergement assure un accompagnement éducatif et quotidien, dans une ambiance familiale
        et bienveillante, pour les enfants dont le trajet scolaire est trop long, dont la famille a besoin
        d’un relais, ou dans certaines situations de placement.
    </p>

    <p>
        L’équipe veille à ce que chaque jeune puisse s’épanouir, évoluer à son rythme et développer
        son autonomie dans les gestes du quotidien, tout en maintenant un lien régulier avec sa famille.
    </p>

    <article aria-labelledby="che-cecite-public-heading">
        <h3 id="che-cecite-public-heading">Public accueilli</h3>
        <ul>
            <li>Enfants et adolescents atteints de déficience visuelle, parfois avec troubles associés (intellectuels, médicaux ou comportementaux)</li>
            <li>Âges : de 3 à 23 ans, selon le parcours et les dérogations scolaires</li>
            <li>Groupes de vie : <strong>Les P’tits Polissons</strong> (enfants, profils plus dépendants) et <strong>Les Phénix</strong> (adolescents/jeunes, profils plus autonomes)</li>
            <li>Possibilité de placements SPJ/SAJ, en hébergement permanent ou structure week-end mutualisée (types 6 et 7)</li>
        </ul>
    </article>
</section>

