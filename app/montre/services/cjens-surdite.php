<section class="tight" aria-labelledby="missions-heading">
    <h2 id="missions-heading">Missions et valeurs</h2>
    <ul>
        <li>Favoriser la communication sous toutes ses formes : langue des signes, lecture labiale, gestes naturels, pictogrammes.</li>
        <li>Offrir un cadre bienveillant, rassurant et respectueux du rythme de chacun.</li>
        <li>Encourager l’enfant à être acteur de sa vie quotidienne et à faire ses propres choix.</li>
        <li>Développer la confiance en soi et la participation active.</li>
        <li>Soutenir les familles dans la compréhension du handicap et la mise en place d’un projet cohérent.</li>
    </ul>
</section>

