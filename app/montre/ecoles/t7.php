<section class="tight" aria-labelledby="admission-heading">
    <h2 id="admission-heading">Modalités d’admission</h2>

    <p><strong>Public :</strong> enfants de 3 à 12 ans présentant une déficience auditive (diagnostic requis).</p>

    <h3>Procédure</h3>
    <ol>
        <li>Orientation par un PMS et dossier médical adapté</li>
        <li>Premier contact avec la direction de l’école</li>
        <li>Rencontre avec la famille et visite de l’établissement</li>
        <li>Analyse du dossier par l’équipe pluridisciplinaire</li>
        <li>Validation de l’admission et préparation de l’accueil</li>
    </ol>

    <p>
        Certains profils médicaux peuvent nécessiter une orientation vers une structure plus adaptée.
    </p>
