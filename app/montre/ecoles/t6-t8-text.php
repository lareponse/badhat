    <h2 id="pedagogie-heading">Accompagnement et pédagogie</h2>

    <p>
      À l’IRSA, nous veillons à ce que chaque enfant puisse apprendre à son rythme, dans un cadre rassurant et attentif.
      Les classes sont volontairement à effectif réduit : cela nous permet d’observer finement les besoins, d’ajuster les
      objectifs au quotidien et de construire des repères stables qui favorisent les apprentissages.
    </p>

    <p><strong>L’accompagnement se précise ensuite selon le profil de l’enfant.</strong></p>

    <section aria-labelledby="pedago-t6">
      <strong id="pedago-t6">Déficience visuelle ou cécité (<abbr title="Type 6">T6</abbr>)</strong>
      <p>
        L’équipe met en place des compensations pour rendre les supports accessibles et développer des stratégies efficaces,
        à l’école comme dans la vie quotidienne.
      </p>
    </section>

    <section aria-labelledby="pedago-t8">
      <strong id="pedago-t8">Trouble du spectre de l’autisme (<abbr title="Type 8">T8</abbr>)</strong>
      <p>
        Nous privilégions des approches structurées et des outils concrets, pensés pour soutenir la communication,
        l’organisation et la compréhension des situations.
      </p>
    </section>

    <section aria-labelledby="pedago-t2">
      <strong id="pedago-t2">Déficiences multiples (<abbr title="Type 2">T2</abbr>)</strong>
      <p>
        L’accompagnement se veut global : nous consolidons les repères, renforçons les compétences fonctionnelles et
        valorisons chaque progrès, en tenant compte de l’ensemble des besoins.
      </p>
    </section>

    <p>
      Cette démarche repose sur un travail d’équipe au plus près de l’enfant. Éducateurs et professionnels paramédicaux
      (logopédie, kinésithérapie, psychomotricité, etc.) coordonnent leurs interventions pour que les objectifs scolaires et les
      objectifs d’autonomie avancent ensemble. Notre fil conducteur reste clair : soutenir les apprentissages fondamentaux et préparer,
      progressivement, l’entrée au secondaire.
    </p>

    <ul>
      <li>Classes à effectif réduit pour un suivi individualisé</li>
      <li>Travail pluridisciplinaire avec éducateurs et paramédicaux (logopédie, kinésithérapie, psychomotricité, etc.)</li>
      <li>Objectif : soutenir les apprentissages fondamentaux et préparer l’entrée au secondaire</li>
    </ul>
  </section>

  <section class="tight" aria-labelledby="services-heading">
    <h2 id="services-heading">Services associés</h2>

    <p>
      Les suivis paramédicaux font partie intégrante de la vie scolaire et s’organisent dans le temps d’école, afin de préserver un
      rythme cohérent pour l’enfant. Cette continuité est renforcée par la collaboration avec le centre de services IRSA, qui permet
      d’approfondir certains besoins spécialisés et d’assurer un accompagnement bien articulé.
    </p>

    <p>
      Au-delà de la classe, des activités éducatives et des sorties sont proposées pour développer l’autonomie, encourager la
      socialisation et apprendre à évoluer dans différents environnements. Ces moments sont pensés comme des occasions concrètes de
      grandir, de prendre confiance et de transférer les acquis du quotidien.
    </p>

    <ul>
      <li>Interventions paramédicales intégrées dans le temps scolaire</li>
      <li>Collaboration avec le centre de services IRSA pour renforcer les suivis spécialisés</li>
      <li>Activités éducatives et sorties pour l’autonomie et la socialisation</li>
    </ul>
  </section>

  <section class="tight" aria-labelledby="admission-heading">
    <h2 id="admission-heading">Modalités d’admission</h2>

    <p>
      L’école s’adresse aux enfants de 3 à 12 ans présentant une déficience visuelle, un trouble du spectre de l’autisme ou une
      déficience multiple. Un diagnostic est nécessaire afin de garantir un accompagnement réellement adapté et cohérent avec les
      besoins de l’enfant.
    </p>

    <p>
      L’admission se déroule en plusieurs étapes, de façon simple et claire. En général, une orientation par un <abbr title="Centre psycho-médico-social">PMS</abbr>,
      accompagnée d’un dossier médical adapté, constitue le point de départ. Un premier échange est ensuite organisé avec la direction
      de l’école, avant une rencontre et une visite de l’établissement. Enfin, l’équipe pluridisciplinaire analyse la situation et valide
      la décision d’admission, dans l’intérêt de l’enfant et de son parcours.
    </p>

    <ol>
      <li>Orientation par un <abbr title="Centre psycho-médico-social">PMS</abbr> et dossier médical adapté</li>
      <li>Premier contact avec la direction de l’école</li>
      <li>Rencontre et visite de l’établissement</li>
      <li>Décision d’admission validée par l’équipe pluridisciplinaire</li>
    </ol>

    <p><strong>Certains profils médicaux trop lourds peuvent nécessiter une orientation vers une structure plus adaptée.</strong></p>
  </section>

