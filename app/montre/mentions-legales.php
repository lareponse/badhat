    <h3>2. Données personnelles traitées</h3>
    <p>
        Le site <strong>www.irsa.be</strong> ne procède à aucune collecte automatique de données personnelles.
        Des données peuvent être communiquées de manière volontaire par l'utilisateur, notamment via :
