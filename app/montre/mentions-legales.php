    <p>Les données sont traitées exclusivement pour :</p>
    <ul>
        <li>répondre aux demandes formulées,</li>
        <li>assurer le suivi institutionnel ou administratif y afférent.</li>
    </ul>
    <p>Aucune donnée n'est utilisée à des fins commerciales, publicitaires ou de profilage.</p>

    <h3>4. Base juridique du traitement</h3>
    <p>Le traitement repose sur :</p>
    <ul>
        <li>le consentement libre, spécifique et éclairé de la personne concernée,</li>
        <li>et/ou l'intérêt légitime d'IRSA ASBL à répondre aux sollicitations reçues.</li>
    </ul>

    <h3>5. Destinataires des données</h3>
    <p>
        Les données personnelles sont accessibles uniquement aux membres habilités d'IRSA ASBL.
        Elles ne font l'objet d'aucune communication à des tiers, sauf obligation légale.
    </p>

    <h3>6. Durée de conservation</h3>
    <p>
        Les données sont conservées pendant la durée strictement nécessaire au traitement de la demande, puis supprimées
        ou archivées conformément aux obligations légales.
    </p>

    <h3>7. Sécurité des données</h3>
    <p>
        IRSA ASBL met en œuvre des mesures techniques et organisationnelles appropriées afin de garantir la sécurité
        et la confidentialité des données personnelles.
    </p>
